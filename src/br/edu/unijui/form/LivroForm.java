    private void PopulateTableLivros(ArrayList<Livro> livros) {
        model.setRowCount(0);
        for (Livro obj : livros) {
            Object rowData[] = new Object[3];
            rowData[0] = obj.getId();
            rowData[1] = obj.getTitulo();
            rowData[2] = obj.getAutor();
            model.addRow(rowData);
        }
    }

